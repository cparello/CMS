function get_help_text(ht)   {

switch(ht)   {
case 1:
   helpText = "This will be text describing the owners message to the members.";                      
   return helpText;    
break;
case 2:
   helpText = "This will be text describing the description of the owner.";                      
   return helpText;    
break;
case 3:
   helpText = "This will be the owners name.";                      
   return helpText;    
break;
case 4:
   helpText = "This will be the name of the file with the owners photo.";                      
   return helpText;    
break;
}

}
