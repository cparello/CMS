function get_help_text(ht)   {

switch(ht)   {
case 1:
   helpText = "The information of the products.";                      
   return helpText;    
break;
case 2:
   helpText = "Select a product.";                      
   return helpText;    
break;
case 3:
   helpText = "Update the description of the product.";                      
   return helpText;    
break;
case 4:
   helpText = "Select the image file of the product."                      
   return helpText;    
break;


}

}
