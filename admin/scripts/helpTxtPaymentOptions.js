function get_help_text(ht)   {

switch(ht)   {
case 1:
   helpText = "Use these options to designate the types of payment options that can be used on your sales forms i.e.  if your business accepts \"Cash\", select the \"Cash\" check box, for \"Credit Cards\" select the \"Credit\" check box. ";                   
   return helpText;    
break;
case 2:
   helpText = "Use these options to designate the types of monthly payments allowed for various services on your sales forms.";                      
   return helpText;  
break;
}

}
